package com.geektrust.model;

public class Kingdom {

	private String name;
	
	private String emblem;

	public Kingdom(String name, String emblem) {
		super();
		this.name = name;
		this.emblem = emblem;
	}

	public String getName() {
		return name;
	}

	public String getEmblem() {
		return emblem;
	}
	
	
}
